if velocidade_cidadao == 80:
    print("O senhor estava no limite da velocidade kkkk \nEstá brincando com a sorte")
elif velocidade_cidadao < 80:
    print("O senhor está na velocidade certa. \nPode continuar sua trajetória")
else:
    print("O senhor está acima da velocidade e está levando uma multa por isso!!")